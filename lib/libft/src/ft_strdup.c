#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	size_t	size;
	char	*dest;

	size = ft_strlen(src);
	dest = malloc((sizeof(char) * size) + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}
